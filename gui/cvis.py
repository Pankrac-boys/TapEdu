#This function finds and highlights object contours on images. It accepts two arguments: img being the image on which we want to look for contours and imgContour being
#the  image on which we want to draw them
def getContours(img, imgContour):
    contours,hierarchy = cv2.findContours(img,cv2.RETR_EXTERNAL,cv2.CHAIN_APPROX_NONE)
    for cnt in contours:
        area = cv2.contourArea(cnt)
        print(area)
        cv2.drawContours(imgContour, cnt, -1, (255, 0, 0), 3)
        x, y, w, h = cv2.boundingRect(cv2.approxPolyDP(cnt,0.02*cv2.arcLength(cnt,True),True))
        cv2.rectangle(imgContour,(x,y),(x+w,y+h),(0,255,0),2)   
    return imgContour

